package Model;

/**
 *
 * @author pc
 */
public class Team {
    int id;
    String name;
    String image;
    String details;
    int status;
    int clubID;

    public Team() {
    }

    public Team(int id, String name, String image, String details, int status, int clubID) {
        this.id = id;
        this.name = name;
        this.image = image;
        this.details = details;
        this.status = status;
        this.clubID = clubID;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDetails() {
        return details;
    }

    public void setDetails(String details) {
        this.details = details;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getClubID() {
        return clubID;
    }

    public void setClubID(int clubID) {
        this.clubID = clubID;
    }
    
    
}
